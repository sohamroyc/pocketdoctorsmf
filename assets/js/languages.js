// Language management functions
class LanguageManager {
  constructor() {
    this.currentLanguage = localStorage.getItem('language') || 'en';
    this.init();
  }

  init() {
    this.loadLanguage(this.currentLanguage);
    this.setupLanguageToggle();
  }

  loadLanguage(lang) {
    if (!translations[lang]) {
      lang = 'en'; // Fallback to English
    }
    
    this.currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
      const key = element.getAttribute('data-translate');
      if (translations[lang][key]) {
        if (element.tagName === 'INPUT' && element.type === 'text' || element.tagName === 'INPUT' && element.type === 'email' || element.tagName === 'INPUT' && element.type === 'password') {
          element.placeholder = translations[lang][key];
        } else if (element.tagName === 'TEXTAREA') {
          element.placeholder = translations[lang][key];
        } else {
          element.textContent = translations[lang][key];
        }
      }
    });

    // Update page title
    if (translations[lang]['page-title']) {
      document.title = translations[lang]['page-title'];
    }

    // Update language toggle button
    const langToggle = document.querySelector('[data-lang-toggle]');
    if (langToggle) {
      const langNames = { en: 'EN', hi: 'à¤¹à¤¿à¤‚', bn: 'à¦¬à¦¾à¦‚' };
      langToggle.innerHTML = `ğŸŒ ${langNames[lang]}`;
    }
  }

  setupLanguageToggle() {
    const langToggle = document.querySelector('[data-lang-toggle]');
    if (langToggle) {
      langToggle.addEventListener('click', () => {
        const languages = ['en', 'hi', 'bn'];
        const currentIndex = languages.indexOf(this.currentLanguage);
        const nextIndex = (currentIndex + 1) % languages.length;
        this.loadLanguage(languages[nextIndex]);
      });
    }
  }

  getTranslation(key) {
    return translations[this.currentLanguage][key] || translations['en'][key] || key;
  }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
  window.languageManager = new LanguageManager();
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { translations, LanguageManager };
}
